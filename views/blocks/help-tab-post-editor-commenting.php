<p>
	This tab can be used to set up Muut commenting on the page (or post). It is the only tab that is visible on all post types (not just pages).
</p>
<ul>
	<li>
		<p><strong>Enable Muut commenting</strong>
			Check this to enable Muut commenting for the page or post. If on a page, it will deactivate the other two tabs.</p>
	</li>
	<li>
		<p><strong>Flat</strong> commenting
			A commenting section where all the posts appear in a single thread and will be listed in chronological order from top to bottom.</p>
	</li>
	<li>
		<p><strong>Threaded</strong> commenting
			A commenting section where top level comments can have one level of replies. The threads with the newest replies appear on top.</p>
	</li>
	<li>
		<p><strong>Disable image uploads</strong>
			Linking to external images is still possible when uploads are disabled. Images are always displayed in gallery view.</p>
	</li>
</ul>