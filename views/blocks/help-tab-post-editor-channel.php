<p>
	This tab is used to embed a single channel at this location. It acts like Channels on the main forum, but the forum's navigation is not visible. It will generate the channel path and name by default, based on the page slug, or you can enter a custom channel path if you want to embed an existing one, for example.
</p>
<ul>
	<li>
		<p><strong>Enable channel</strong> (a standalone discussion area)
			Check this to enable a standalone Muut channel embed for the page. This will deactivate the other two tabs.</p>
	</li>
	<li>
		<p><strong>Hide online users</strong>
			Hides the "Online users" section. Online status (green dot) is still shown in the avatars for users who didn't hide it.</p>
	</li>
	<li>
		<p><strong>Disable image uploads</strong>
			Linking to external images is still possible when uploads are disabled. Images are always displayed in gallery view.</p>
	</li>
</ul>