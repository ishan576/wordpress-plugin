<p>
	By using this tab, you will embed the full Muut forum using the rich user interface, which also allows a truly customizable look and feel using CSS.
</p>
<ul>
	<li>
		<p><strong>Enable forum</strong>
			Check this to enable the embed for the full Muut forum. It includes Muut's rich interface that is completely customizable with CSS.</p>
	</li>
	<li>
		<p><strong>Hide online users</strong>
			Hides the "Online users" section. Online status (green dot) is still shown in the avatars for users who didn't hide it.</p>
	</li>
	<li>
		<p><strong>Disable image uploads</strong>
			Linking to external images is still possible when uploads are disabled. Images are always displayed in gallery view.</p>
	</li>
	<li>
		<p><strong>Show Comments</strong>
			Shows a channel called <em>Comments</em> at the bottom of the forum navigation that contains every active commenting section from posts and pages on the website. Comment threads that a user participated in or watched will always show up in her personal "My feed" regardless of this setting.</p>
	</li>
</ul>